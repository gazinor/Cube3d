void	set_dir(t_data *data, char c)
{
	data->bonus.dir.x = -1 * (c == 'S' ? -1 : c == 'N');
	data->bonus.dir.y = -1 * (c == 'E' ? -1 : c == 'W');
	data->bonus.planx = 0.66 * (c == 'W' ? -1 : c == 'E');
	data->bonus.plany = 0.66 * (c == 'S' ? -1 : c == 'N');
}

void	*ft_init_map(t_data *data)
{
	int	i;
	int	j;

	i = 0;
	while (G_BUFFER[i])
		i++;
	if (!(data->bonus.map = malloc(sizeof(char *) * (i + 1))))
		return (NULL);
	data->map_mid.x = i / 2.;
	i = 0;
	while (G_BUFFER[i])
	{
		j = 0;
		while (G_BUFFER[i][j])
			j++;
		if (!(data->bonus.map[i] = malloc(sizeof(char) * (j + 1))))
			return (NULL);
		if (data->map_mid.y < j)
			data->map_mid.y = j / 2.;
		i++;
	}
	ft_fill_map(data);
	return ((void*)0);
}

void	ft_fill_map(t_data *data)
{
	int	i;
	int	j;

	i = 0;
	while (G_BUFFER[i])
	{
		j = 0;
		while (G_BUFFER[i][j])
		{
			data->bonus.map[i][j] = G_BUFFER[i][j];
			if (G_BUFFER[i][j] == 'N' || G_BUFFER[i][j] == 'S' ||
					G_BUFFER[i][j] == 'E' || G_BUFFER[i][j] == 'O')
			{
				set_dir(data, G_BUFFER[i][j]);
				data->bonus.map[i][j] = '0';
				data->bonus.pos.x = (double)i + 0.5;
				data->bonus.pos.y = (double)j + 0.5;
				data->perso.depart.x = (double)i + 0.5;
				data->perso.depart.y = (double)j + 0.5;
			}
			j++;
		}
		data->bonus.map[i][j] = '\0';
		i++;
	}
	data->bonus.map[i] = 0;
}
